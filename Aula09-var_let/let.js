// ECMAScript 2015 (ES6)

var nome = 'Kayne';
var nome = 'Kayane';

console.log(nome);

// Variáveis do tipo "var" são obsoletas e não são mais utilizadas
// pois a mesma permite que uma variável de mesmo nome seja,
// redeclarada o que é um comportamento não desejado.
// O ECMAScript (ES6) corrigiu esse problema criando a variável "let"

let nome2 = 'Kayne';
// let nome2 = 'Kayane';

console.log(nome2);